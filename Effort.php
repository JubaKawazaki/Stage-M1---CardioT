                                <h2>2. Quelles sont les indications ?</h2>
                                <p>Le<strong> test d’effort</strong> permet d’aider au diagnostic des maladies des artères coronaires. Cet examen permet également de dépister de très nombreuses maladies du muscle du cœur tels que les troubles du rythme cardiaque survenant à l’effort et permet d’évaluer la capacité physique des patients. <br><br>

                                    En cas de positivité du test d’effort, le médecin peut : <br>
                                    • soit traiter directement par des médicaments <br>
                                    • soit confirmer le diagnostic par une coronarographie qui est un examen qui permettera de faire l’inventaire précis de l’état des artères coronaire de faire éventuellement un traitement approprié : par médicament, par angioplastie coronaire ou par pontages aorto-coronariens.</p><br>

                                <div class="section-title">
                                    <h2>3. Comment se déroule l'examen ?</h2>
                                    <p>Pendant l’examen, le patient est torse nu sur le vélo ou le Tapis d’épreuve d’effort. Le médecin dispose un certain nombre d’électrodes sur le patient, à des endroits bien précis.

                                        Un premier enregistrement de l’électrocardiogramme est effectué au repos. Le patient débute ensuite le test à l’effort.

                                        Pendant l’examen, le médecin contrôle la pression artérielle et l’électrocardiogramme.
                                    </p>
                                    <br>
                                    <div class="section-title">
                                        <h2>Les risques de l'examen</h2>
                                        <p>En pratique, le test d’effort est un examen à faible risque si les contre-indications sont respectées. La complication principale est l’apparition d’une arythmie maligne qui doit être prise en charge très rapidement. A cet effet, le Cabinet Médical dispose d’un matériel de réanimation (défibrillateur) dans la salle d’examen.

                                            La durée de cet examen dépend donc de la capacité du patient à réaliser l’effort, mais en règle générale une épreuve d’effort dure de 10 à 15 minutes.</p><br>
