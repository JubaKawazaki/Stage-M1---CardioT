                            <h2>1. Qu'est-ce que L'épreuve d'effort ?</h2>
                            <p>L’épreuve d’effort, ou test d’effort est un examen complémentaire cardiologique consistant en l’enregistrement
                                d’un électrocardiogramme pendant le déroulement d’un exercice physique calibré et à charge croissante. Le protocole de l’épreuve d’effort employé est établi à l’aide d’un vélo ou cyclo-ergomètre, ou d’un tapis d’épreuve d’effort. <br><br>

                                Les paramètres enregistrés sont principalement le pouls, la tension artérielle, et surtout l’électrocardiogramme numérisé permettant une relecture et un monitoring plus précis à la recherche de toute anomalie survenant à l’effort. <br><br>

                                Les principales indications sont essentiellement la recherche de signes d’ischémie myocardique à l’effort pouvant orienter vers une pathologie coronaire (rétrécissement au niveau des artères qui nourrissent cœur).</p><br><br>

