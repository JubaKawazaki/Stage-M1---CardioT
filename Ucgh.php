                                <h2>2. Quelles sont les indications ?</h2>
                                <p>Cet examen permet de rechercher des arythmies cardiaques et de déceler de nombreuses maladies cardiovasculaires. Cet examen peut être particulièrement utile dans le cadre du bilan de malaises ou de vertiges,
                                    de même que dans le cadre de la recherche événements rythmique paroxystique. <br><br>
                                    Le médecin se sert de cet examen comme un outil parmi d’autres qui lui permettent d’apporter des arguments pour étayer son diagnostic.</p><br>

                                <div class="section-title">
                                    <h2>3. Comment se déroule l'examen ?</h2>
                                    <p>Le médecin ou l’infirmière dispose sur le patient un certain nombre d’électrodes reliées à l’appareil. Le patient conserve l’appareil pour un enregistrement continu pendant 24 heures dans les conditions habituelles de vie. <br><br>

                                        Le lendemain le médecin interprétera le résultat de l’examen, vous disposerai alors d’un compte rendu dans les 24h. <br><br>

                                        Cet examen est totalement indolore.</p><br>
