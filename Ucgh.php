                            <h2>1. Qu'est-ce que le Holter ECG de 24h ?</h2>
                            <p>L’enregistrement continu de l’électrocardiogramme ou Holter ECG est un technique de mesure automatique de l’activité électrique du cœur pendant 24 heures,
                                à l’aide d’un appareil miniaturisé.</p><br>
                            <p><strong>L'appareil de Holter ECG est constitué d'électrodes reliées à un système d’enregistrement et de stockage électronique des données miniaturisé.</strong></p>
