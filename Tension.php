                        <div class="section-title">
                            <h2>1. Qu'est-ce que le monitoring tensionnel ?</h2>
                            <p><strong>Le monitoring tensionnel ou Monitoring Ambulatoire de la Pression Artérielle</strong>
                                (appelé couramment<strong>MAPA</strong> ) est un appareil de mesure automatique la pression artérielle de manière répétée pendant 24 h. <br><br>

                                <strong>Le MAPA</strong> est constitué d’un brassard gonflable relié à un manomètre par un tuyau souple qui est solidarisé avec un système de stockage électronique des données miniaturisé.
                            </p><br>
                            <img src="assets/img/hyyp.jpg" alt="Description de l'image">
                            <br>
                            <br><br><br>


                            <div class="section-title">
                                <h2>2. Quelles sont les indications ?</h2>
                                <p>Le <strong>MAPA</strong> permet de mieux appréhender les chiffres tensionnels et de différencier une hypertension artérielle permanente d’une hypertension artérielle occasionnelle, retrouvée uniquement au cours de la consultation et pouvant être secondaire au stress ( effet blouse blanche ). <br><br>

                                    Le<strong> MAPA</strong> permet également de classifier certaines hypertensions et d’en évaluer la sévérité (par exemple, l’absence de baisse des chiffres durant le sommeil est un critère péjoratif). <br><br>

                                    Le<strong> MAPA</strong> permet, par ailleurs, d’évaluer l’efficacité d’un traitement anti-hypertenseur .</p><br>

                                <div class="section-title">
                                    <h2>3. Comment se déroule l'examen ?</h2>
                                    <p>Le brassard est placé sur le bras du patient, le bloc pompe-manomètre-stockage numérique, sous forme d’un petit boîtier, étant mis en bandoulière. <br><br>

                                        Il est réglé pour prendre la pression artérielle plusieurs fois par heure le jour (deux à trois fois en règle générale) et un peu moins souvent la nuit (une a deux fois par heure). <br><br>

                                        Les mesures sont analysées par le cardiologue.</p><br>
