                                <h2>2. Quelles sont les indications ?</h2>
                                <p>Cet examen permet d’explorer le cœur et fournit au cardiologue de très nombreux renseignements. Les très grandes majorités des maladies cardiaques nécessitent la réalisation d’une échocardiographie pour leur diagnostic précis.
                                    Le médecin se sert de cet examen comme un outil parmi d’autres, permettant d’apporter des arguments pour étayer son diagnostic.</p><br>

                                <div class="section-title">
                                    <h2>3. Comment se déroule l'examen ?</h2>
                                    <p>Le patient, torse nu, est allongé sur le côté gauche habituellement sur la table d’examen.
