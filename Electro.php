                <h2>2. Quelles sont les indications ?</h2>
                <p>Cet examen permet de dépister de très nombreuses maladies cardiaques tels que l’infarctus du myocarde,
                  les troubles du rythme cardiaques… Le médecin se sert de cet examen comme un outil parmi d’autres,
                  permettant d’apporter des arguments pour étayer son diagnostic.</p><br>

                <div class="section-title">
                  <h2>3. Comment se déroule l'examen ?</h2>
                  <p>Le médecin place un certain nombre d’électrodes sur le patient, à des endroits bien précis. Les électrodes sont reliées à l’électrocardiographe
